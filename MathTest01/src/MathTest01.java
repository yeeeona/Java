
// Math 클래스: 수학에 관한 다양한 메소드와 상수에관해 정의해 놓은 클래스
// 클래스 메소드, 클래스 상수만 존재함.
// final 클래스 
public class MathTest01 {
	public static void main(String[] args) {
		System.out.println(Math.abs(-10)); // 절대값
		System.out.println(Math.sqrt(9)); // 루트값
		System.out.println(Math.PI); // 원주율
		System.out.println(Math.pow(2, 3)); // 승수
		System.out.println("-------");
		System.out.println(Math.ceil(10.1)); // 올림
		System.out.println(Math.ceil(10.5));
		System.out.println(Math.ceil(10.0)); // 0은 올리지 않음.
		System.out.println(Math.ceil(-10.1)); // 음수일 때
		System.out.println(Math.floor(7.1)); // 내림
		System.out.println(Math.floor(-7.1));
		System.out.println(Math.round(5.5)); // 반올림
		System.out.println(Math.round(-5.5));
		System.out.println("-------");
		System.out.println(Math.rint(1.4)); //
		System.out.println(Math.rint(1.6));
		System.out.println("-------");
		System.out.println(Math.round(1.5));
		System.out.println(Math.round(2.544));
		System.out.println(Math.round(3.5));
		System.out.println(Math.round(4.5));
		System.out.println(Math.round(5.5));
		System.out.println(Math.round(6.5));
		System.out.println(Math.round(7.5));
		System.out.println(Math.round(8.5));
		System.out.println(Math.round(9.5));
		System.out.println("-------");
		System.out.println(Math.rint(1.5));
		System.out.println(Math.rint(2.5));
		System.out.println(Math.rint(3.5));
		System.out.println(Math.rint(4.5));
		System.out.println(Math.rint(5.5));
		System.out.println(Math.rint(6.5));
		System.out.println(Math.rint(7.5));
		System.out.println(Math.rint(8.5));
		System.out.println(Math.rint(9.5));
		System.out.println("=========");
		
	}
}
